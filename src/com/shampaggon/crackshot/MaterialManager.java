    public static Material getMaterial(final String value) {
        if (MaterialManager.pre113) {
            final String[] data = value.split("~");
            try {
                final Method method = Material.class.getMethod("getMaterial", Integer.TYPE);
                return (Material)method.invoke(Material.class, Integer.parseInt(data[0]));
            }
            catch (Exception e) {
                return Material.getMaterial(data[0].toUpperCase());
            }
        }
        String enumName = value;
        if (isId(value)) {
            enumName = MaterialManager.map.get(value + (enumName.contains("~") ? "" : "~0"));
        }
        return Material.getMaterial(enumName.toUpperCase());
    }
    
    private static boolean isId(final String value) {
        if (value.contains("~")) {
            return true;
        }
        try {
            Integer.parseInt(value);
        }
        catch (NumberFormatException e) {
            return false;
        }
        return true;
    }
    
    public static Material getSkullBlock() {
        return Material.getMaterial(MaterialManager.pre113 ? "SKULL" : "PLAYER_WALL_HEAD");
    }
    
    public static boolean isPressurePlate(final Block block) {
        return block.getType().toString().toUpperCase().endsWith("_PLATE");
    }
    
    public static boolean isSkullBlock(final Block block) {
        final String typeStr = block.getType().toString();
        return typeStr.equalsIgnoreCase("SKULL") || typeStr.equalsIgnoreCase("PLAYER_WALL_HEAD");
    }
    
    public static boolean isSkullItem(final Material type) {
        final String typeStr = type.toString();
        return typeStr.equalsIgnoreCase("SKULL_ITEM") || typeStr.equalsIgnoreCase("PLAYER_HEAD");
    }
